public class Lista {
    private String voce;
    private String pc;
    
    public Lista(){
        this.voce = null;
        this.pc = null;
    }

    public String getVoce() {
        return voce;
    }

    public void setVoce(String voce) {
        this.voce = voce;
    }

    public String getPc() {
        return pc;
    }

    public void setPc(String pc) {
        this.pc = pc;
    }
    
    
}
